package pubsub

import (
	"bytes"
	"context"
	"encoding/gob"

	amqp "github.com/rabbitmq/amqp091-go"
)

func PublishGob[T any](ch *amqp.Channel, exchange, key string, val T) error {
	var gob_byte bytes.Buffer
	enc := gob.NewEncoder(&gob_byte)
	err := enc.Encode(val)
	if err != nil {
		return err
	}

	err = ch.PublishWithContext(
		context.Background(),
		exchange,
		key,
		false,
		false,
		amqp.Publishing{
			ContentType: "application/gob",
			Body: gob_byte.Bytes(),
		},
	)
	if err != nil {
		return err
	}

	return nil
}